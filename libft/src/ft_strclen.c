int			ft_strclen(char *str, char *str2)
{
	int i;
	int	j;

	if (!str || !*str || !str2 || !*str2)
		return (0);
	i = 0;
	while (str[i])
	{
		j = 0;
		while (str2[j])
		{
			if (str[i] == str2[j])
			{
				i++;
				return (i);
			}
			j++;
		}
		i++;
	}
	return (0);
}
